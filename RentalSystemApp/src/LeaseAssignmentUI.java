private void populateRenterDropdown() {
    cmbRenter.removeAllItems();
    String sql = "SELECT renter_id, username FROM Renter";
    try (java.sql.Connection conn = DBConnection.getConnection();
         java.sql.Statement stmt = conn.createStatement();
         java.sql.ResultSet rs = stmt.executeQuery(sql)) {
        
        while (rs.next()) {
            // Displays "1 - johndoe"
            cmbRenter.addItem(rs.getInt("renter_id") + " - " + rs.getString("username"));
        }
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading renters: " + ex.getMessage());
    }
}


