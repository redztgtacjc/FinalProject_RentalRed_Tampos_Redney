    private void btnIssueLeaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueLeaseActionPerformed
        // TODO add your handling code here:
        String renterSelection = (String) cmbRenter.getSelectedItem();
String unitSelection = (String) cmbUnit.getSelectedItem();
String startDate = txtStartDate.getText().trim();
String endDate = txtEndDate.getText().trim();

if (renterSelection == null || unitSelection == null || startDate.isEmpty() || endDate.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please complete all lease criteria fields.");
    return;
}

// Extract the ID number from our formatted dropdown text (splits at the first space)
int renterId = Integer.parseInt(renterSelection.split(" ")[0]);
int unitId = Integer.parseInt(unitSelection.split(" ")[0]);

// SQL query to save into the Lease table (Table 3)
String sql = "INSERT INTO Lease (renter_id, unit_id, start_date, end_date) VALUES (?, ?, ?, ?)";

try (java.sql.Connection conn = DBConnection.getConnection();
     java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {
    
    pstmt.setInt(1, renterId);
    pstmt.setInt(2, unitId);
    pstmt.setString(3, startDate); // Matches YYYY-MM-DD database layout format
    pstmt.setString(4, endDate);
    
    pstmt.executeUpdate();
    javax.swing.JOptionPane.showMessageDialog(this, "Lease agreement issued successfully!");
    
    txtStartDate.setText("");
    txtEndDate.setText("");
} catch (Exception ex) {
    // Triggers an error alert if the room or renter is already occupied (due to our UNIQUE constraints!)
    javax.swing.JOptionPane.showMessageDialog(this, "Lease Assignment Failed: That renter or unit already has an active lease!");
}
    }
private void populateUnitDropdown() {
        cmbUnit.removeAllItems(); 
        
        String sql = "SELECT unit_id, unit_number FROM RentalUnit"; 
        
        try (java.sql.Connection conn = DBConnection.getConnection();
             java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
             java.sql.ResultSet rs = pstmt.executeQuery()) {
            
            while (rs.next()) {
                int id = rs.getInt("unit_id");
                String unitNum = rs.getString("unit_number");
                
                cmbUnit.addItem(new UnitItem(id, unitNum));
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading units: " + ex.getMessage());
        }
        } // End of your previous code

    class UnitItem { 
        private int id;
        private String unitNumber;

        public UnitItem(int id, String unitNumber) {
            this.id = id;
            this.unitNumber = unitNumber;
        }

        public int getId() {
            return id;
        }

        @Override
        public String toString() {
            return unitNumber; 
        }
    
        
    }//GEN-LAST:event_btnIssueLeaseActionPerformed

